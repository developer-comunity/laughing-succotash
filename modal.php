  <h3>Que es?</h3>
  <p>El Parque Fundidora, es un parque público localizado en la ciudad de Monterrey, Nuevo León, se encuentra en los terrenos que ocupó la compañía Fundidora de Fierro y Acero de Monterrey de 1900 a 1986.
Parque Fundidora es testimonio vivo de la historia de Monterrey, un acervo patrimonial del Estado en el cual conviven naturaleza, tradición y cultura para el goce de la comunidad. Los visitantes ahora podrán apreciar las 27 macropiezas industriales entre las que se cuentan los dos Hornos Altos, edificios, chimeneas y 127 piezas más de menor tamaño, distribuidas en los jardines y andadores del Parque.

Su superficie de 142 hectáreas brinda a los visitantes diversas áreas destinadas para el descanso, la recreación y la convivencia, brindando un espacio para realizar actividades multifacéticas como el deporte y el entretenimiento, así como actividades económicas, culturales y eventos de todo tipo, que son el eje para el desarrollo y esparcimiento de las personas.
